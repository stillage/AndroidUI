package com.example.mantan.model;

public class makanan {
    String nama, kecil, sedang, besar, diskon;
    int gambar;


    public makanan(){}

    public String getNama() {
        return nama;
    }

    public int getGambar() {
        return gambar;
    }

    public String getKecil() {
        return kecil;
    }

    public String getSedang() {
        return sedang;
    }

    public String getBesar() {
        return besar;
    }

    public String getDiskon() {
        return diskon;
    }

}